<h1>Ikatyros NY - Yrityksemme ehdot</h1>

<p>
    Me, Ikatyros NY, ja sen alaiset toimijat ja työntekijät, toteutamme erilaisia multimediapalveluita ("tuotteita" tai "palveluita"),
    jotka asiakas saa meiltä arvioimamme aikataulun mukaisesti.
    Palveluiden jälkeiset ehdot astuvat voimaan, kun palvelumme ovat toteuttu suurin piirtein solmitun toimeksiantosopimuksen ("sopimuksen") mukaisesti,
    tai jos sopimus raukeaa.
    Pidätämme oikeuden päättää KAIKKI palveluihimme liittyvät tulkinnan varassa olevat yksityiskohdat jälkikäteen;
    ja kun sopimus on solmittu, tuoden ne myös asiakkaalle selkeästi esiin.
    Luettelemme tulkinnan varaisiksi yksityiskohdiksi ne seikat, jotka eivät ole näissä ehdoissa tai sopimuksessa mainittuna.
    Teemme asiakkaan tilauksen perusteella yksityiskohtaisen sopimuksen, jossa lukee kaikki tilauksen oleelliset tiedot,
    ja jonka asiakas sitten allekirjoittaa varmistaakseen palveluiden tekoprosessin sisäiset yksityiskohdat.
    Ennen kuin asiakas saa sopimuksen, on hän hyväksynyt nämä ehdot ja saanut antamaansa sähköpostiin meiltä tilausvahvistuksen,
    jonka ohessa kyseessä oleva sopimus on.
    Asiakkaan halutessa muuttaa toteutettavaa palvelun kokonaisuutta, täytyy hänen ilmoittaa siitä palvelun tarjoajalle,
    jolloin tämän sopimuksen aikataulu ja kokonaishinta uudelleenarvioidaan (ehdot säilyvät).
    
    <br><br>
    Palvelun tai tuotteen myyjän ja ostajan välille ei solmita työsuhdetta.
    <br><br>

    Palvelun tuottavat NY –yrittäjänä toimivat Mikkelin lukion opiskelijat.
    NY –yritys toimii NY vuosi yrittäjänä –ohjelman mukaisesti maksimissaan yhden vuoden ajan.
    Asiakas on tietoinen tästä seikasta, ja on huomioinut sen mm. vastuun ja takuun osalta.

    Sopimusta voidaan tarpeen vaatiessa muuttaa osapuolten yhteisellä päätöksellä. Muutos on tehtävä kirjallisesti.
</p>

<h2>Laskutus</h2>
<p>
    Laskuillamme on 14 päivän maksuaika. Toimitamme laskun mahdollisimman pian sen jälkeen, kun olemme saaneet toimitettua tilatun palvelun asiakkaalle.
    Annamme maksumuistutuksen, jolloin asiakas voi perusteltuaan sopia erillisen maksuajan korkeintaan kuukauden lisäajalta,
    ja jolloin NY –yritys on vielä lisäaikajan jälkeen kuukauden toiminnassa, josta laskutamme viisi (5) euroa.
    Jos asiakas on vielä muistutuksen jälkeenkin kykenemätön maksamaan kokonaishinnan suuruista summaa maksuajan sisään,
    perimme maksun kokonaishinnan suuruisesta summasta puuttuvan rahamäärän suuruisen summan takaisin oikeudellisesti,
    josta esiintyvistä kustannuksista on vastuussa asiakas.
    Veroton myynti: NY-yritys ei ole alv-velvollinen (vähäinen liiketoiminta).
</p>

<h2>Ajankohta</h2>
<p>
    Jos toteutuksessa esiintyy viivästystä mistä tahansa syistä, joita emme pystyneet ottamaan huomioon ennen aikataulun arviointia,
    asiakas hyväksyy tämän ja sallii meille lisäaikaa meille tarvittavan määrän.
    Ilmoitamme viivästymisistä avoimesti asiakkaalle mitä pikimmiten ja pyrimme toteuttamaan sovitun palvelun mahdollisimman pian ongelmista huolimatta.
</p>

<h2>Vastuu</h2>
<p>
    Yrityksemme palvelut ovat meidän tuottamiamme ja ME otamme vastuun tuottamistamme palveluistamme tai niiden aiheuttamista vahingoista,
    sillä ehdolla, että me tietoisesti olemme laiminlyöneet tuotteemme laatua. Tarvittaessa, sopimuksessa lukee vastuun yksityiskohdat tarkemmin.
</p>

<h2>Palveluidemme takuu</h2>
<p>
    Jokaisen tuottamamme palvelun takuu on oletuksena mitätön, ellei toisin mainita. Asiakkaalle luovutettuna asiakas ottaa itse vastuun siitä, jos tuote katoaa, hajoaa tai tuhoutuu oman toimintansa takia.
    Voimme toki auttaa asiakasta ongelmien sattuessa, mutta pidätämme oikeuden päättää siitä itse.
</p>

<h2>Asiakkaan multimedian julkaisu</h2>
<p>
Asiakas hyväksyy, että kaikki materiaalit ja niissä mahdollisesti esiintyvä asiakkaan multimedia, johon kuuluu muun muassa graafiset-, kuva-, ääni- ja videosisällöt,
pysyvät meillä hallussa vain palvelun tarjoavaa mahdollisia mainostustarkoituksia varten, kuten palvelun tarjoajan omilla nettisivuilla ja sosiaalisessa mediassa.
Emme julkaise niitä muualla, emmekä uudelleenkäytä niitä muilla mahdollisilla asiakkailla, ilman tämän asiakkaan erillistä lupaa.
</p>

<h2>Tietoturva</h2>
<p>
    Pidämme kaiken asiakkaalle arkaluontoisen sisällön täysin salassa. Toteutettuamme palvelumme asiakas voi pyytää hävittämään kaiken arkaluontoisen sisällön.
    Osapuolet sitoutuvat pitämään salassa tämän sopimuksen perusteella kaikki toiselta osapuolelta saamansa tiedot ja taidot,
    jotka osapuoli on muutoin tämän sopimuksen perusteella tai siihen liittyen saanut haltuunsa, joita on niiden luonteen vuoksi pidettävä luottamuksellisina.
</p>

<h2>Sopimuksen purkaminen</h2>
<p>
<b>Osapuolilla on oikeus purkaa kirjallisesti sopimus heti päättyväksi, mikäli:</b>
<br>
- toteutus ei edisty solmitun sopimuksen mukaisesti tai erikseen myöhemmin sovitun suunnitelman tai aikataulun mukaisesti,
<br>
- osapuoli muulla tavoin olennaisesti rikkoo toiminnallaan sopimusta,
<br>
- toimitusta ei voi toteuttaa osapuolista riippumattomista syistä.
<br>
Perusteettomiksi perusteiksi luetaan kaikki muut paitsi edellä mainitut perustelut.

Tämän sopimuksen purkautuessa myyjä veloittaa ostajaa kustannuksista, jotka ovat kertyneet purkautumishetkeen asti.

Asiakkaan purkaessa sopimuksen halutessaan tai perusteettomasti, on hän velvollinen maksamaan 40% kokonaishinnasta ennen viimeistelyvaihetta,
jonka jälkeen hän on velvollinen maksamaan 80% kokonaishinnasta.
Tähän sopimukseen liittyvät mahdolliset erimielisyydet pyritään ratkaisemaan sopimusosapuolten välisin keskinäisin neuvotteluin.
Ellei sovintoon päästä, asia ratkaistaan Mikkelin käräjäoikeudessa.
</p>

